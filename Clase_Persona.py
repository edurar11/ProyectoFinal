class Persona():
    Nombre=None
    Apellidos=None
    Direccion=None
    Telefono=None
    Edad=None
    Correo=None
    RFC=None
    Contrasena=None
    CodPos=None
    
    def __init__(self,Nombre,Apellidos,Direccion,Telefono,Edad,Correo,RFC,Contrasena,CodPos):
        self.Nombre=Nombre
        self.Apellidos=Apellidos
        self.Direccion=Direccion
        self.Telefono=Telefono
        self.Edad=Edad
        self.Correo=Correo
        self.RFC=RFC
        self.Contrasena=Contrasena
        self.CodPos=CodPos